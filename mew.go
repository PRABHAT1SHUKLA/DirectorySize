package main

import "fmt"

func main() {
	// Variable declaration with type
	var name string = "Alice"

	// Type inference
	age := 30

	// Multiple variable declarations
	var a, b, c int = 1, 2, 3

	// Constants
	const pi = 3.14159

	fmt.Println("Name:", name)
	fmt.Println("Age:", age)
	fmt.Println("a, b, c:", a, b, c)
	fmt.Println("Pi:", pi)
}